package vlog.vo;

public class GalleryVo {
	private int    gallery_no   ;
	private String orp_no       ;
	private String gallery_title;
	private String gallery_date ;
	private String gallery_cont ;
	
	public int getGallery_no() {
		return gallery_no;
	}
	public void setGallery_no(int gallery_no) {
		this.gallery_no = gallery_no;
	}
	public String getOrp_no() {
		return orp_no;
	}
	public void setOrp_no(String orp_no) {
		this.orp_no = orp_no;
	}
	public String getGallery_title() {
		return gallery_title;
	}
	public void setGallery_title(String gallery_title) {
		this.gallery_title = gallery_title;
	}
	public String getGallery_date() {
		return gallery_date;
	}
	public void setGallery_date(String gallery_date) {
		this.gallery_date = gallery_date;
	}
	public String getGallery_cont() {
		return gallery_cont;
	}
	public void setGallery_cont(String gallery_cont) {
		this.gallery_cont = gallery_cont;
	}
	
}
