package vlog.vo;

public class AboardVO {
	private int volun_no;
	private String orp_no;
	private String gen_id;
	private String volun_title;
	private String volun_date;
	private String volun_push;
	public int getVolun_no() {
		return volun_no;
	}
	public void setVolun_no(int volun_no) {
		this.volun_no = volun_no;
	}
	public String getOrp_no() {
		return orp_no;
	}
	public void setOrp_no(String orp_no) {
		this.orp_no = orp_no;
	}
	public String getGen_id() {
		return gen_id;
	}
	public void setGen_id(String gen_id) {
		this.gen_id = gen_id;
	}
	public String getVolun_title() {
		return volun_title;
	}
	public void setVolun_title(String volun_title) {
		this.volun_title = volun_title;
	}
	public String getVolun_date() {
		return volun_date;
	}
	public void setVolun_date(String volun_date) {
		this.volun_date = volun_date;
	}
	public String getVolun_push() {
		return volun_push;
	}
	public void setVolun_push(String volun_push) {
		this.volun_push = volun_push;
	}
}
