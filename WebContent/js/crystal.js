

function stdCrystal(){
	$.ajax({
		url : '/Vlog/StdCrystal.do',
		data : fdata,
		type : 'post',
		success : function(res){
			//alert(res.sw);
			//modal닫기
			$('#writeModal').modal('hide');
			
			//modal안의 입력 내용 지우기 
			$('.intxt').val("");
			
		},
		error : function(xhr){
//			alert("상태: " + xhr.status);
			alert('회원정보 수정 성공!');
		},
		dataType : 'json'
	})
	
}
